# The '\t' is used to add a tab space for alignment, and '\n' adds a newline after the header.
print("Item\t\tPrice\n")
# The '\t' adds a tab space between the item names and prices for alignment.
print("Apple\t\t$1.75")
print("Banana\t\t$2.25")
print("Cherry\t\t$3.50")
# '\n' before the line provides space between the items and the separator.
print("\n---------- -------")
# '\t' aligns "Total" with the other items and prices.
print("Total\t\t$7.50")